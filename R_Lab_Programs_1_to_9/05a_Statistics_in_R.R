# STATISTICS IN R

x <- c(10,20,30,40,50)

mean_value <- mean(x)
median_value <- median(x)
max_value <- max(x)
min_value <- min(x)
sum_value <- sum(x)

cat("Mean =", mean_value, "\n")
cat("Median =", median_value, "\n")
cat("Maximum =", max_value, "\n")
cat("Minimum =", min_value, "\n")
cat("Sum =", sum_value, "\n")
